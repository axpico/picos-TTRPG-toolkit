type StatBlockInput = z.input<typeof statBlock>;

/** Slugify a name into a stable id so re-seeding/re-loading is deterministic. */
const slug = (s: string): string =>
  s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "") || "entry";

/** Build a stat-block entry (trait/action/reaction/legendary) with a stable id. */
const entry = (name: string, desc: string): StatEntry => ({ id: slug(name), name, desc });

/** Parse a partial stat block into a fully-defaulted one. */
const sb = (input: StatBlockInput): StatBlock => statBlock.parse(input);

